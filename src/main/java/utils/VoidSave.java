package utils;

import java.util.List;

public class VoidSave {
    public int strength;
    public int dexterity;
    public int focus;
    public List<String> relicList;

    public VoidSave(int strength, int dexterity, int focus, List<String> relicList) {
        this.strength = strength;
        this.dexterity = dexterity;
        this.focus = focus;
        this.relicList = relicList;
    }
}
